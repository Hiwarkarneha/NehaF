class Multiarray {
    public static void main(String[] args) {
        int marr[][] = { { 12, 21 }, { 43, 65 } };
        for (int i = 0; i < 2; i++) {
            for (int j = 0; j < 2; j++) {
                System.out.println(marr[i][j]+ "  ");

            }
            System.out.println();
        }
    }

}